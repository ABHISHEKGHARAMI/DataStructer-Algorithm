// print the number 1 - 100 using the loop 
#include<iostream>
#include<cmath>
#include<algorithm>
using namespace std;
int main(){
    cout << "printing the 1 -100 using the loop:";
    for (int i = 1; i <=100; i++)
    {
        cout << "\n" << i;
    }
    
    return 0;
}
