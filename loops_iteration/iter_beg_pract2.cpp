// print the multiplication table of 5
#include<iostream>
using namespace std;
int main(){
    cout << "print the multiplication table of the 5 :";
    for (int i = 1; i <= 10; i++)
    {
        // creating the table
        cout <<"\n" << 5 << " * " << i << " = " << 5*i;
    }
    
    return 0;
}